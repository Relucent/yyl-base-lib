package com.github.relucent.base.common.constants;

/**
 * 常量字符串
 */
public class StringConstants {

    /** 空字符串 */
    public static final String EMPTY = "";

    /** 水平制表符(horizontal tab) [0x09] */
    public static final String TAB = "\t";

    /** 换行 (line feed) [0x0A] */
    public static final String LF = "\n";

    /** 回车(carriage return) [0x0D] */
    public static final String CR = "\r";

    /** 空格(space) [0x20] */
    public static final String SPACE = " ";

    /** 双引号 [0x22] */
    public static final String DOUBLE_QUOTES = "\"";

    /** 和号[0x26] */
    public static final String AMPERSAND = "&";

    /** 单引号 [0x27] */
    public static final String SINGLE_QUOTE = "'";

    /** 开括号 [0x28] */
    public static final String PARENTHESES_START = "(";

    /** 闭括号 [0x29] */
    public static final String PARENTHESES_END = ")";

    /** 星号[0x2A] */
    public static final String STAR = "*";

    /** 加号[0x2B] */
    public static final String PLUS = "+";

    /** 逗号[0x2C] */
    public static final String COMMA = ",";

    /** 减号(破折号)[0x2D] */
    public static final String DASHED = "-";

    /** 句号(点)[0x2E] */
    public static final String DOT = ".";

    /** 斜杠[0x2F] */
    public static final String SLASH = "/";

    /** 冒号[0x3A] */
    public static final String COLON = ":";

    /** 分号[0x3B] */
    public static final String SEMICOLON = ";";

    /** 大于 [0x3C] */
    public static final String GREATER_THAN = ">";

    /** 等号 [0x3D] */
    public static final String EQUAL = "=";

    /** 小于 [0x3E] */
    public static final String LESS_THAN = "<";

    /** 问号 [0x3E] */
    public static final String QUESTION = "?";

    /** 电子邮件符号[0x40] */
    public static final String AT = "@";

    /** 开方括号[0x5B] */
    public static final String BRACKET_START = "[";

    /** 反斜杠[0x5C] */
    public static final String BACKSLASH = "\\";

    /** 闭方括号[0x5D] */
    public static final String BRACKET_END = "]";

    /** 下划线[0x5F] */
    public static final String UNDERLINE = "_";

    /** 反单引号[0x60] */
    public static final String BACKQUOTE = "`";

    /** 开花括号(分隔符开始)[0x7B] */
    public static final String DELIM_START = "{";

    /** 或号(垂线)[0x7C] */
    public static final String OR = "|";

    /** 闭花括号(分隔符结束)[0x7D] */
    public static final String DELIM_END = "}";

    /** 波浪号 [0x7E] */
    public static final String TILDE = "~";


    /** 中文(全角)空格 */
    public static final String FULL_WIDTH_SPACE = "　";

    /** 中文(全角)逗号 */
    public static final String FULL_WIDTH_COMMA = "，";

    /** 中文(全角)冒号 */
    public static final String FULL_WIDTH_COLON = "：";

    /** 中文(全角)分号 */
    public static final String FULL_WIDTH_SEMICOLON = "；";

    /** 中文(全角)句号 */
    public static final String FULL_WIDTH_DOT = "。";

    /** NULL字符串 */
    public static final String NULL = "null";
    /** 回车换行 */
    public static final String CRLF = "\r\n";

    /** HTML转义字符，空格 */
    public static final String HTML_NBSP = "&nbsp;";
    /** HTML转义字符，和号(&amp;) */ // &
    public static final String HTML_AMP = "&amp;";
    /** HTML转义字符，双引号(") */
    public static final String HTML_QUOTE = "&quot;";
    /** HTML转义字符，单引号(') */
    public static final String HTML_APOS = "&apos;";
    /** HTML转义字符，小于(&lt;) */ // <
    public static final String HTML_LT = "&lt;";
    /** HTML转义字符，大于(&gt;) */ // >
    public static final String HTML_GT = "&gt;";

    /** JSON对象字符串({})，表示空对象 */
    public static final String JSON_EMPTY_OBJECT = "{}";
}
