package com.github.relucent.base.common.time;

/**
 * 时间单位
 */
public enum DateUnit {
    /** 年份 */
    YEAR,
    /** 半年 */
    HALFYEAR,
    /** 季度 */
    QUARTER,
    /** 月份 */
    MONTH,
    /** 日期 */
    DATE,
    /** 小时(0-23) */
    HOUR_OF_DAY,
    /** 分钟(0-59) */
    MINUTE,
    /** 秒钟(0-59) */
    SECOND;
}
