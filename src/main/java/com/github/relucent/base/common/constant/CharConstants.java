package com.github.relucent.base.common.constant;

/**
 * 常量字符
 */
public class CharConstants {

    /** 水平制表符(horizontal tab) [0x09] */
    public static final char TAB = '\t';

    /** 换行 (line feed) [0x0A] */
    public static final char LF = '\n';

    /** 回车(carriage return) [0x0D] */
    public static final char CR = '\r';

    /** 空格(space) [0x20] */
    public static final char SPACE = ' ';

    /** 双引号 [0x22] */
    public static final char DOUBLE_QUOTES = '"';

    /** 和号[0x26] */
    public static final char AMPERSAND = '&';

    /** 单引号 [0x27] */
    public static final char SINGLE_QUOTE = '\'';

    /** 开括号 [0x28] */
    public static final char PARENTHESES_START = '(';

    /** 闭括号 [0x29] */
    public static final char PARENTHESES_END = ')';

    /** 星号[0x2A] */
    public static final char STAR = '*';

    /** 加号[0x2B] */
    public static final char PLUS = '+';

    /** 逗号[0x2C] */
    public static final char COMMA = ',';

    /** 减号(破折号)[0x2D] */
    public static final char DASHED = '-';

    /** 句号(点)[0x2E] */
    public static final char DOT = '.';

    /** 斜杠[0x2F] */
    public static final char SLASH = '/';

    /** 冒号[0x3A] */
    public static final char COLON = ':';

    /** 分号[0x3B] */
    public static final char SEMICOLON = ';';

    /** 大于 [0x3C] */
    public static final char GREATER_THAN = '>';

    /** 等号 [0x3D] */
    public static final char EQUAL = '=';

    /** 小于 [0x3E] */
    public static final char LESS_THAN = '<';

    /** 问号 [0x3E] */
    public static final char QUESTION = '?';

    /** 电子邮件符号[0x40] */
    public static final char AT = '@';

    /** 开方括号[0x5B] */
    public static final char BRACKET_START = '[';

    /** 反斜杠[0x5C] */
    public static final char BACKSLASH = '\\';

    /** 闭方括号[0x5D] */
    public static final char BRACKET_END = ']';

    /** 下划线[0x5F] */
    public static final char UNDERLINE = '_';

    /** 反单引号[0x60] */
    public static final char BACKQUOTE = '`';

    /** 开花括号(分隔符开始)[0x7B] */
    public static final char DELIM_START = '{';

    /** 或号(垂线)[0x7C] */
    public static final char OR = '|';

    /** 闭花括号(分隔符结束)[0x7D] */
    public static final char DELIM_END = '}';

    /** 波浪号 [0x7E] */
    public static final char TILDE = '~';

    /** 中文(全角)空格 */
    public static final char FULL_WIDTH_SPACE = '　';

    /** 中文(全角)逗号 */
    public static final char FULL_WIDTH_COMMA = '，';

    /** 中文(全角)冒号 */
    public static final char FULL_WIDTH_COLON = '：';

    /** 中文(全角)分号 */
    public static final char FULL_WIDTH_SEMICOLON = '；';

    /** 中文(全角)句号 */
    public static final char FULL_WIDTH_DOT = '。';

    /** UTF-8 BOM FEFF(65279) */
    public static final char UTF_8_BOM = 0xFEFF;

    private CharConstants() {
    }
}
