package com.github.relucent.base.common.constant;

/**
 * 数字常量
 */
public class NumberConstant {

    /** {@code Byte}类型，常数零(0) */
    public static final Byte BYTE_ZERO = Byte.valueOf((byte) 0);
    /** {@code Byte}类型，常数一(1) */
    public static final Byte BYTE_ONE = Byte.valueOf((byte) 1);
    /** {@code Byte}类型，常数负一 (-1) */
    public static final Byte BYTE_MINUS_ONE = Byte.valueOf((byte) -1);

    /** {@code Integer}类型，常数零(0) */
    public static final Integer INTEGER_ZERO = Integer.valueOf(0);
    /** {@code Integer}类型，常数一(1) */
    public static final Integer INTEGER_ONE = Integer.valueOf(1);
    /** {@code Integer}类型，常数二 (2) */
    public static final Integer INTEGER_TWO = Integer.valueOf(2);
    /** {@code Integer}类型，常数负一 (-1) */
    public static final Integer INTEGER_MINUS_ONE = Integer.valueOf(-1);

    /** {@code Long}类型，常数零(0) */
    public static final Long LONG_ZERO = Long.valueOf(0L);
    /** {@code Long}类型，常数一(1) */
    public static final Long LONG_ONE = Long.valueOf(1L);
    /** {@code Long}类型，常数负一 (-1) */
    public static final Long LONG_MINUS_ONE = Long.valueOf(-1L);

    /** {@code Short}类型，常数零(0) */
    public static final Short SHORT_ZERO = Short.valueOf((short) 0);
    /** {@code Short}类型，常数一(1) */
    public static final Short SHORT_ONE = Short.valueOf((short) 1);
    /** {@code Short}类型，常数负一 (-1) */
    public static final Short SHORT_MINUS_ONE = Short.valueOf((short) -1);

    /** Reusable Float constant for zero. */
    public static final Float FLOAT_ZERO = Float.valueOf(0.0f);
    /** Reusable Float constant for one. */
    public static final Float FLOAT_ONE = Float.valueOf(1.0f);
    /** Reusable Float constant for minus one. */
    public static final Float FLOAT_MINUS_ONE = Float.valueOf(-1.0f);

    /** {@code Double}类型，常数零(0) */
    public static final Double DOUBLE_ZERO = Double.valueOf(0.0d);
    /** {@code Double}类型，常数一(1) */
    public static final Double DOUBLE_ONE = Double.valueOf(1.0d);
    /** {@code Double}类型，常数负一 (-1) */
    public static final Double DOUBLE_MINUS_ONE = Double.valueOf(-1.0d);
}
